import type { NFLTeamType } from "./NFLdata";

export type GameType = {
  Away: NFLTeamType,
  Home: NFLTeamType,
  Week: number,
  Code: string,
  Winner: NFLTeamType | undefined | null,
}

export type GamePickedType = {
  Away: NFLTeamType,
  Home: NFLTeamType,
  Week: number,
  Code: string,
  Winner: NFLTeamType,
}

export type GameWinner = Pick<GameType, "Code" | "Winner">

export type NFLScheduleType = typeof NFLscheduleData;

export function checkAllGamesPicked(schedule: GameType[]): boolean {
  let allChecked = true;
  schedule.forEach((game) => {
    if(!game.Winner){
      allChecked = false;
    }
  })
  return allChecked;
}

export function fullPickedSchedule(schedule: GameType[]): GamePickedType[] {
  const finalSchedule: GamePickedType[] = [];
  schedule.forEach((game) => {
    finalSchedule.push({
      ...game,
      Winner: game.Winner ?? game.Away
    })
  });
  return finalSchedule;
}

export function randomPickedSchedule(schedule: GameType[]): GamePickedType[] {
  const finalSchedule: GamePickedType[] = [];
  schedule.forEach((game) => {
    finalSchedule.push({
      ...game,
      Winner: Math.random() > 0.5 ? game.Away : game.Home
    })
  });
  return finalSchedule;
}

export function GameTest(schedule: GameType[]) {
  const newSchedule = schedule.map((game) => {
    const pickWinner = Math.random() > 0.5 ? game.Away : game.Home;
    const newGame: GameWinner = {Code: game.Code, Winner: pickWinner};
    return newGame;
  });
  return newSchedule;
}

